# TODO: Use Werkzeug to hash the user's password when creating a new user.
@app.route('/register', methods=["POST", "GET"])
def register():
    regis_form = RegisterForm()
    if regis_form.validate_on_submit():

        user_search = db.session.execute(db.select(User).where(User.email == regis_form.email.data))
        user = user_search.scalar()
        if not user:
            hash_salted_pass = generate_password_hash(
                password=regis_form.password.data,
                method='pbkdf2:sha256',
                salt_length=8
            )
            new_user = User(
                name=regis_form.name.data,
                email=regis_form.email.data,
                password=hash_salted_pass,
            )
            db.session.add(new_user)
            db.session.commit()
            login_user(new_user)
            return redirect(url_for("get_all_posts"))

        else:
            flash("This email address already exists, please use log in instead.")
    return render_template("register.html", form=regis_form, current_user=current_user)



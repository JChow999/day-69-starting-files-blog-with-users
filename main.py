@app.route('/login', methods=["GET", "POST"])
def login():
    login_form = LoginForm()
    if login_form.validate_on_submit():
        user_search = db.session.execute(db.select(User).where(User.email == login_form.email.data))
        user = user_search.scalar()

        if user:
            if check_password_hash(user.password, login_form.password.data):
                login_user(user)
                return redirect("/")
            else:
                flash("Incorrect password, please try again.")
                redirect(url_for("login"))
        else:
            flash("That email is does not exist, please try again.")
            redirect(url_for("login"))
    return render_template("login.html", login_form=login_form, current_user=current_user)


